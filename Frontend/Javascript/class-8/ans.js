//push()

let color = ["black", "white", "blue", "red"];
console.log(color);
let addcolor = "yellow";
color.push(addcolor);
console.log(color);

//pop()

let number = ["4", "45", "78", "34"];
console.log(number);
number.pop();
console.log(number);

//shift()

let fruits = ["apple", "orange", "mango", "lemon"];
console.log(fruits);
fruits.shift();
console.log(fruits);

//unshift()

let animal = ["cat", "dog", "tiger", "lion"];
console.log(animal);
animal.unshift("elephant");
console.log(animal);
